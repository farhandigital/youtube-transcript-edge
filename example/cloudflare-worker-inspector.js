/**
 * cloudflare-worker-inspector.js
 *
 * Example Cloudflare Worker that uses inspectPlayerResponseDebug to debug
 * why a video might not have captions available.
 *
 * Deploy this and call it like:
 *   https://your-worker.dev/api/transcript?videoId=xoriGNUNF7E
 *   https://your-worker.dev/api/transcript?videoId=https://www.youtube.com/watch?v=xoriGNUNF7E
 *
 * Query parameters:
 *   - videoId (required): YouTube video ID or full URL
 *   - debug (optional): Set to 'false' to disable debugging. Default: true
 *   - raw (optional): Set to 'true' to include full HTML and JSON responses. Default: false
 */

import {
	fetchTranscript,
	inspectPlayerResponse,
	inspectPlayerResponseDebug,
} from 'youtube-transcript-edge';

export default {
	async fetch(request, _env, _ctx) {
		const url = new URL(request.url);
		const videoId = url.searchParams.get('videoId');
		const debug = url.searchParams.get('debug') !== 'false'; // Default to true
		const includeRawData = url.searchParams.get('raw') === 'true'; // Default to false

		if (!videoId) {
			return new Response(
				JSON.stringify(
					{
						error: 'Missing videoId query parameter',
						examples: [
							'?videoId=xoriGNUNF7E',
							'?videoId=https://www.youtube.com/watch?v=xoriGNUNF7E',
							'?videoId=xoriGNUNF7E&debug=false',
							'?videoId=xoriGNUNF7E&raw=true (includes full HTML and JSON)',
						],
					},
					null,
					2,
				),
				{ status: 400, headers: { 'Content-Type': 'application/json' } },
			);
		}

		try {
			// Use debug version if requested, otherwise use simple version
			const inspection = debug
				? await inspectPlayerResponseDebug(videoId, undefined, {
						includeRawData,
					})
				: await inspectPlayerResponse(videoId);

			// Try to fetch the transcript too
			let transcriptResult = null;
			try {
				const transcript = await fetchTranscript(videoId);
				transcriptResult = {
					success: true,
					transcriptLength: transcript.length,
					firstSegment: transcript[0],
				};
			} catch (error) {
				transcriptResult = {
					success: false,
					error: error instanceof Error ? error.message : String(error),
				};
			}

			return new Response(
				JSON.stringify(
					{
						videoId: inspection.videoId,
						inspection: {
							playabilityStatus: inspection.playabilityStatus,
							captionTracks: inspection.captionTracks,
							translationLanguages: inspection.translationLanguages,
							hasAutoGeneratedCaptions: inspection.hasAutoGeneratedCaptions,
							error: inspection.error,
							...(debug && {
								debug: inspection.debug,
							}),
						},
						transcriptResult,
					},
					null,
					2,
				),
				{
					headers: {
						'Content-Type': 'application/json',
					},
				},
			);
		} catch (error) {
			return new Response(
				JSON.stringify(
					{
						error: 'Unexpected error',
						message: error instanceof Error ? error.message : String(error),
					},
					null,
					2,
				),
				{ status: 500, headers: { 'Content-Type': 'application/json' } },
			);
		}
	},
};
